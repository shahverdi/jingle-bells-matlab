function y = note(note_name,Octave,Length)
FR = ["A","Bb","B","C","Db","D","Eb","E","F","F#","G","Ab","An"];
fR = logspace(log10(220),log10(2)+log10(220),13);
Fr = find(FR==note_name);
fr = fR(Fr)*2^Octave;
fs = 10000;
bpm = 100;
L = Length*60/bpm;
t = 1:fs/2*L; t = t/fs;
d = interp1([1 0.05*fs 0.2*fs 0.3*fs 0.5*fs]*L,[0.0 0.7 1 0.3 0],1:fs/2*L,'PCHIP');
if Fr==13
    d = 0*d;
end
y = d.*sin(2*pi*t*fr);
end

